// == (implicit comparation)

const number = 6;
const text = "6";

console.log(number == text);

console.log(number === text);

// typeof
console.log(typeof number);
console.log(typeof text);

// == compares just the values
// === compares the value and the data type

// explicit conversion
Number();
String();

